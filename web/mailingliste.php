"subscribe" (Betreff kann leer sein) an <a
href="mailto:skripte-request@listserv.uni-jena.de">
skripte-request@listserv.uni-jena.de</a> zu schicken.

Commits werden auf der Mailinglsite <a href="mailto:skripte-commit@listserv.uni-jena.de">
skripte-commit@listserv.uni-jena.de</a> angekündigt.
Anmeldung wie oben, aber bei der Adresse <a
href="mailto:skripte-commit-request@listserv.uni-jena.de">skripte-commit-request@listserv.uni-jena.de</a>.
</p>

